     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $nofifications = Notification::where('user_id', auth()->id());
        if ($nofifications->exists()) {
            $nofifications = $nofifications->with(
                'user',
                function ($q) {
                    $q->select('id', 'first_name', 'last_name', 'email', 'phone', 'bio', 'gender');
                }
            )->get();
            return $this->success($nofifications);
        } else {
            return $this->errorOk('No Nofications yet!');
        }
    }

